FIELDS = ["title", "year", "genre", "publisher", "exe", "setup", "note"]
YEAR_MIN = 1980
YEAR_MAX = 2099


@dataclass
class Record:
    dir: str
    exe: str
    title: str
    year: str
    genre: str
    publisher: str
    note: str
    needs_review: bool
    candidates: list[str]


def parse_args() -> argparse.Namespace:
    ap = argparse.ArgumentParser(description="Local metadata editor for SETUP-REVIEW.json")
    ap.add_argument(
        "--launcher-dir",
        type=Path,
        help="Launcher directory containing SETUP-REVIEW.json",
    )
    ap.add_argument(
        "--review-file",
        type=Path,
        help="Explicit SETUP-REVIEW.json path (overrides --launcher-dir)",
    )
    ap.add_argument("--host", default="127.0.0.1", help="Bind host (default: 127.0.0.1)")
    ap.add_argument("--port", type=int, default=8765, help="Bind port (default: 8765)")
    ap.add_argument("--no-browser", action="store_true", help="Do not auto-open browser")
    return ap.parse_args()


def normalize_text(value: Any, maxlen: int | None = None) -> str:
    text = str(value or "").replace("\r", " ").replace("\n", " ").strip()
    if maxlen is not None:
        text = text[:maxlen]
    return text


def normalize_year(value: Any) -> str:
    year = normalize_text(value, maxlen=4)
    if year == "":
        return ""
    if not year.isdigit():
        raise ValueError("year must be numeric")
    y = int(year)
    if y < YEAR_MIN or y > YEAR_MAX:
        raise ValueError(f"year must be between {YEAR_MIN} and {YEAR_MAX}")
    return year


def load_review(path: Path) -> dict[str, Any]:
    if not path.is_file():
        raise FileNotFoundError(f"review file not found: {path}")
    data = json.loads(path.read_text(encoding="utf-8"))
    if "records" not in data or not isinstance(data["records"], list):
        raise ValueError("invalid review file: missing records array")
    return data


def write_game_txt(game_dir: Path, record: dict[str, Any]) -> None:
    meta: dict[str, str] = {
        "title": normalize_text(record.get("title"), 80),
        "year": normalize_year(record.get("year")),
        "genre": normalize_text(record.get("genre"), 40),
        "publisher": normalize_text(record.get("publisher"), 80),
        "exe": normalize_text(record.get("exe"), 64),
        "setup": normalize_text(record.get("setup"), 64),
        "note": normalize_text(record.get("note"), 120),
    }

    lines = ["# DOS Game Browser metadata - edited by tools/metadata-ui.py", ""]
    for key in FIELDS:
        if meta[key] != "":
            lines.append(f"{key}={meta[key]}")
    text = "\n".join(lines) + "\n"
    (game_dir / "GAME.TXT").write_text(text, encoding="utf-8")


def compute_needs_review(record: dict[str, Any]) -> bool:
    return any(normalize_text(record.get(k)) == "" for k in ("title", "year", "publisher"))


class MetadataApp:
    def __init__(self, review_file: Path):
        self.review_file = review_file
        self.lock = threading.Lock()
        self.payload = load_review(review_file)
        self.records = self.payload["records"]
        self.launcher_dir = self._resolve_launcher_dir()
        self.scan_root = self._resolve_scan_root()

    def _resolve_launcher_dir(self) -> Path:
        launcher = self.payload.get("launcher_dir")
        if launcher:
            p = Path(str(launcher)).resolve()
            if p.is_dir():
                return p
        return self.review_file.parent.resolve()

    def _resolve_scan_root(self) -> Path:
        scan_root = self.payload.get("scan_root")
        if scan_root:
            p = Path(str(scan_root)).resolve()
            if p.is_dir():
                return p

        guessed = (self.launcher_dir / "GAMES").resolve()
        if guessed.is_dir():
            return guessed

        dirs: list[Path] = []
        for rec in self.records:
            d = rec.get("dir")
            if d:
                dirs.append(Path(str(d)).resolve().parent)
        if dirs:
            return Path(os.path.commonpath([str(d) for d in dirs])).resolve()

        raise ValueError("cannot infer scan root; provide review generated by setup-image.py")

    def save_review(self) -> None:
        self.review_file.write_text(json.dumps(self.payload, indent=2), encoding="utf-8")

    def state(self) -> dict[str, Any]:
        with self.lock:
            total = len(self.records)
            unresolved = sum(1 for r in self.records if r.get("needs_review", False))
            return {
                "reviewFile": str(self.review_file),
                "scanRoot": str(self.scan_root),
                "launcherDir": str(self.launcher_dir),
                "total": total,
                "unresolved": unresolved,
                "records": self.records,
            }

    def regenerate_index(self) -> dict[str, Any]:
        out_lst = self.launcher_dir / "GAMES.LST"
        cmd = [
            sys.executable,
            str(Path(__file__).resolve().parents[1] / "tools" / "scan-games.py"),
            "--games",
            str(self.scan_root),
            "--out",
            str(out_lst),
        ]
        proc = subprocess.run(cmd, capture_output=True, text=True)
        if proc.returncode != 0:
            detail = (proc.stderr or proc.stdout).strip()
            raise RuntimeError(detail or "scan-games.py failed")
        return {
            "output": str(out_lst),
            "stdout": (proc.stdout or "").strip(),
        }

    def _build_updates(self, rec: dict[str, Any], patch: dict[str, Any]) -> dict[str, str]:
        return {
            "title": normalize_text(patch.get("title", rec.get("title", "")), 80),
            "year": normalize_year(patch.get("year", rec.get("year", ""))),
            "genre": normalize_text(patch.get("genre", rec.get("genre", "")), 40),
            "publisher": normalize_text(patch.get("publisher", rec.get("publisher", "")), 80),
            "exe": normalize_text(patch.get("exe", rec.get("exe", "")), 64),
            "setup": normalize_text(patch.get("setup", rec.get("setup", "")), 64),
            "note": normalize_text(patch.get("note", rec.get("note", "")), 120),
        }

    def update_record(self, idx: int, patch: dict[str, Any]) -> dict[str, Any]:
        with self.lock:
            if idx < 0 or idx >= len(self.records):
                raise IndexError("record index out of range")
            rec = self.records[idx]
            game_dir = Path(rec["dir"])
            if not game_dir.is_dir():
                raise FileNotFoundError(f"game directory not found: {game_dir}")

            updates = self._build_updates(rec, patch)

            rec.update(updates)
            rec["needs_review"] = compute_needs_review(rec)
            write_game_txt(game_dir, rec)
            self.save_review()
            return rec

    def bulk_update(self, ids: list[int], patch: dict[str, Any]) -> dict[str, Any]:
        with self.lock:
            if not ids:
                raise ValueError("ids cannot be empty")

            clean_ids: list[int] = []
            for raw in ids:
                idx = int(raw)
                if idx < 0 or idx >= len(self.records):
                    raise IndexError(f"record index out of range: {idx}")
                clean_ids.append(idx)

            unique_ids = sorted(set(clean_ids))
            updated: list[int] = []
            for idx in unique_ids:
                rec = self.records[idx]
                game_dir = Path(rec["dir"])
                if not game_dir.is_dir():
                    raise FileNotFoundError(f"game directory not found: {game_dir}")

                updates = self._build_updates(rec, patch)
                rec.update(updates)
                rec["needs_review"] = compute_needs_review(rec)
                write_game_txt(game_dir, rec)
                updated.append(idx)

            self.save_review()
            return {"updated": updated, "count": len(updated)}


def make_handler(app: MetadataApp):
    class Handler(BaseHTTPRequestHandler):
        def _json(self, status: int, payload: dict[str, Any]) -> None:
            raw = json.dumps(payload).encode("utf-8")
            self.send_response(status)
            self.send_header("Content-Type", "application/json; charset=utf-8")
            self.send_header("Content-Length", str(len(raw)))
            self.end_headers()
            self.wfile.write(raw)

        def _html(self, html: str) -> None:
            raw = html.encode("utf-8")
            self.send_response(HTTPStatus.OK)
            self.send_header("Content-Type", "text/html; charset=utf-8")
            self.send_header("Content-Length", str(len(raw)))
            self.end_headers()
            self.wfile.write(raw)

        def log_message(self, fmt: str, *args: Any) -> None:
            return

        def do_GET(self) -> None:
            if self.path in ("/", "/index.html"):
                self._html(INDEX_HTML)
                return
            if self.path == "/api/state":
                self._json(HTTPStatus.OK, app.state())
                return
            self._json(HTTPStatus.NOT_FOUND, {"error": "not found"})

        def do_POST(self) -> None:
          if self.path == "/api/regenerate":
            try:
              result = app.regenerate_index()
              self._json(HTTPStatus.OK, {"ok": True, **result})
            except (RuntimeError, ValueError) as exc:
              self._json(HTTPStatus.BAD_REQUEST, {"error": str(exc)})
            return

          if self.path == "/api/bulk-update":
            length = int(self.headers.get("Content-Length", "0"))
            raw = self.rfile.read(length)
            try:
              payload = json.loads(raw.decode("utf-8")) if raw else {}
            except json.JSONDecodeError:
              self._json(HTTPStatus.BAD_REQUEST, {"error": "invalid json"})
              return

            ids = payload.get("ids")
            patch = payload.get("patch", {})
            if not isinstance(ids, list):
              self._json(HTTPStatus.BAD_REQUEST, {"error": "ids must be an array"})
              return
            if not isinstance(patch, dict):
              self._json(HTTPStatus.BAD_REQUEST, {"error": "patch must be an object"})
              return

            try:
              result = app.bulk_update(ids, patch)
              self._json(HTTPStatus.OK, {"ok": True, **result})
            except IndexError as exc:
              self._json(HTTPStatus.NOT_FOUND, {"error": str(exc)})
            except (ValueError, FileNotFoundError) as exc:
              self._json(HTTPStatus.BAD_REQUEST, {"error": str(exc)})
            except Exception as exc:
              self._json(HTTPStatus.INTERNAL_SERVER_ERROR, {"error": f"internal error: {exc}"})
            return

          if not self.path.startswith("/api/record/"):
            self._json(HTTPStatus.NOT_FOUND, {"error": "not found"})
            return

          try:
            idx_str = self.path.rsplit("/", 1)[1]
            idx = int(idx_str)
          except Exception:
            self._json(HTTPStatus.BAD_REQUEST, {"error": "invalid record id"})
            return

          length = int(self.headers.get("Content-Length", "0"))
          raw = self.rfile.read(length)
          try:
            patch = json.loads(raw.decode("utf-8")) if raw else {}
          except json.JSONDecodeError:
            self._json(HTTPStatus.BAD_REQUEST, {"error": "invalid json"})
            return

          try:
            rec = app.update_record(idx, patch)
            self._json(HTTPStatus.OK, {"ok": True, "record": rec})
          except IndexError as exc:
            self._json(HTTPStatus.NOT_FOUND, {"error": str(exc)})
          except (ValueError, FileNotFoundError) as exc:
            self._json(HTTPStatus.BAD_REQUEST, {"error": str(exc)})
          except Exception as exc:
            self._json(HTTPStatus.INTERNAL_SERVER_ERROR, {"error": f"internal error: {exc}"})

    return Handler


