def resolve_review_file(args: argparse.Namespace) -> Path:
    if args.review_file:
        return args.review_file.resolve()
    if not args.launcher_dir:
        raise ValueError("provide either --review-file or --launcher-dir")
    return (args.launcher_dir.resolve() / "SETUP-REVIEW.json")


def main() -> int:
    args = parse_args()

    try:
        review_file = resolve_review_file(args)
        app = MetadataApp(review_file)
    except (ValueError, FileNotFoundError, json.JSONDecodeError) as exc:
        print(f"metadata-ui failed: {exc}")
        return 2

    server = ThreadingHTTPServer((args.host, args.port), make_handler(app))
    url = f"http://{args.host}:{args.port}/"

    print("DGB Metadata UI (Phase 2 MVP)")
    print(f"  review file: {review_file}")
    print(f"  listening:   {url}")
    print("  stop:        Ctrl+C")

    if not args.no_browser:
        try:
            webbrowser.open(url)
        except Exception:
            pass

    try:
        server.serve_forever()
    except KeyboardInterrupt:
        pass
    finally:
        server.server_close()

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
