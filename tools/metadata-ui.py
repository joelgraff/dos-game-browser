"""
Phase 2 MVP: local metadata review/edit UI for setup-image output.

Loads SETUP-REVIEW.json, serves a local web page, and writes updated GAME.TXT
metadata back into each game folder.

Examples:
  python tools/metadata-ui.py --launcher-dir /mnt/dos/DGB
  python tools/metadata-ui.py --review-file /mnt/dos/DGB/SETUP-REVIEW.json --port 8765
"""
